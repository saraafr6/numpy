#Find the determinants of the following matrices
import numpy as np
#a)
a=np.array([[2,1],[3,0]])
print(" det(a) is: ",np.linalg.det(a))

#b)
b=np.array([[0,5,3],[0,1,1],[0,1,0]])
print(" det(b) is: ",np.linalg.det(b))

#c)
c=np.array([[1,2,0],[3,1,1],[4,-2,0]])
print(" det(c) is: ",np.linalg.det(c))

